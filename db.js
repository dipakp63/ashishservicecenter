// db.js — PostgreSQL Database Abstraction Layer using pg
const { Pool } = require('pg');

let pool;
let isInitialized = false;

function initialize() {
  if (pool) return;

  const connectionString = process.env.DATABASE_URL || 'postgresql://postgres:Jivesh%402751@db.jsrqqafwhxcfigpqhumy.supabase.co:5432/postgres';
  
  pool = new Pool({
    connectionString,
    max: 20, // max connections
    idleTimeoutMillis: 30000,
    connectionTimeoutMillis: 5000,
    ssl: { rejectUnauthorized: false }
  });

  console.log('[DB] Connected to Supabase PostgreSQL database.');
}

// Convert SQLite ? placeholders to PostgreSQL $1, $2, etc.
function convertToPg(sql) {
  let count = 1;
  return sql.replace(/\?/g, () => `$${count++}`);
}

async function run(sql, args = []) {
  initialize();
  const pgSql = convertToPg(sql);
  const result = await pool.query(pgSql, args);
  let lastInsertRowid;
  if (result.rows && result.rows.length > 0 && result.rows[0].id) {
    lastInsertRowid = result.rows[0].id;
  }
  return { rowsAffected: result.rowCount, lastInsertRowid };
}

async function get(sql, args = []) {
  initialize();
  const pgSql = convertToPg(sql);
  const result = await pool.query(pgSql, args);
  return result.rows.length > 0 ? result.rows[0] : null;
}

async function all(sql, args = []) {
  initialize();
  const pgSql = convertToPg(sql);
  const result = await pool.query(pgSql, args);
  return result.rows || [];
}

async function batch(statements) {
  initialize();
  const client = await pool.connect();
  try {
    await client.query('BEGIN');
    const results = [];
    for (const s of statements) {
      const pgSql = convertToPg(s.sql);
      const res = await client.query(pgSql, s.args || []);
      results.push({ rowsAffected: res.rowCount });
    }
    await client.query('COMMIT');
    return results;
  } catch (err) {
    await client.query('ROLLBACK');
    throw err;
  } finally {
    client.release();
  }
}

